package com.javaex.ex03;

import java.util.Scanner;

public class Ex04 {

	public static void main(String[] args) {

		Scanner sc = new Scanner(System.in);

		int time, pay;
		
		System.out.print("근무시간:");
		time = sc.nextInt();
		
		if(time<=8) {
			pay = time * 10000;
		} else {
			pay = (8 * 10000) + (time - 8) * 12000;
		}
		
		System.out.println("입금은 " + pay + "원 입니다.");
		
		sc.close();
	}

}
